-- View: Estimates with Lead details
create or replace view public.estimates_with_leads as
select 
  e.*,
  l.client_name,
  l.email as client_email,
  l.phone as client_phone,
  l.project_type,
  l.source as lead_source,
  l.priority,
  l.status as lead_status,
  (select count(*) from public.estimate_items ei where ei.estimate_id = e.estimate_id) as item_count
from public.estimates e
join public.leads l on l.lead_id = e.lead_id;

grant select on public.estimates_with_leads to authenticated;

