-- Dynamic Field Configuration System
-- Allow users to add/remove field options for various dropdowns

create table if not exists public.dynamic_field_options (
  option_id bigint generated by default as identity primary key,
  field_type text not null check (field_type in (
    'lead_source',
    'cost_category',
    'payment_method',
    'project_type',
    'material_category',
    'task_priority',
    'other'
  )),
  option_value text not null,
  display_order int default 0,
  is_active boolean default true,
  color_code text,
  description text,
  created_at timestamp with time zone default now() not null,
  created_by uuid references auth.users(id),
  updated_at timestamp with time zone default now() not null
);

-- Unique constraint to prevent duplicates
create unique index if not exists idx_dynamic_field_unique 
on public.dynamic_field_options(field_type, option_value) 
where is_active = true;

-- Indexes for performance
create index if not exists idx_dynamic_field_type on public.dynamic_field_options(field_type);
create index if not exists idx_dynamic_field_active on public.dynamic_field_options(is_active);
create index if not exists idx_dynamic_field_order on public.dynamic_field_options(display_order);

