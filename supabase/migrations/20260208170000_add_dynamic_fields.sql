-- Updated at trigger
create or replace function public.update_dynamic_field_timestamp()
returns trigger
language plpgsql
as $$
begin
  new.updated_at := now();
  return new;
end;
$$;

drop trigger if exists trg_dynamic_field_updated_at on public.dynamic_field_options;
create trigger trg_dynamic_field_updated_at
before update on public.dynamic_field_options
for each row execute function public.update_dynamic_field_timestamp();

-- RLS policies
alter table public.dynamic_field_options enable row level security;

drop policy if exists "dynamic_field_options_select_auth" on public.dynamic_field_options;
