-- Global Store Inventory System
-- Material master with dynamic quantities, material requests with approval, returns workflow

-- Drop old material_master if exists and create new structure
drop table if exists public.material_master cascade;

-- Materials Master (Dynamic list managed by admin)
create table if not exists public.materials_master (
  material_id bigint generated by default as identity primary key,
  material_name text unique not null,
  description text,
  metric text not null check (metric in ('Litres', 'Kgs', 'Tonnes', 'Sqft', 'Metres', 'Units', 'Pieces', 'Boxes', 'Bags')),
  is_active boolean default true,
  created_at timestamp with time zone default now() not null,
  created_by uuid references auth.users(id)
);

-- Material Quantity Variants (e.g., 20L, 10L, 5L for same material)
create table if not exists public.material_variants (
  variant_id bigint generated by default as identity primary key,
  material_id bigint references public.materials_master(material_id) on delete cascade not null,
  variant_name text not null, -- e.g., "20 Litre Can", "10 Litre Bucket"
  quantity_per_unit numeric(12,3) not null, -- e.g., 20 for 20L can
  is_active boolean default true,
  created_at timestamp with time zone default now() not null
);

-- Store Inventory (Global, Project-independent)
create table if not exists public.store_inventory (
  inventory_id bigint generated by default as identity primary key,
  material_id bigint references public.materials_master(material_id) on delete restrict not null,
  variant_id bigint references public.material_variants(variant_id) on delete restrict not null,
  number_of_units int not null default 0, -- How many units (cans, bags, etc.)
  total_quantity numeric(12,3) not null default 0, -- Total quantity (e.g., 20L x 5 units = 100L)
  location text,
  notes text,
  last_updated timestamp with time zone default now() not null,
  updated_by uuid references auth.users(id)
);

-- Unique constraint: one row per material-variant combination
create unique index if not exists idx_store_inventory_unique 
on public.store_inventory(material_id, variant_id);

-- Material Requests (from projects)
create table if not exists public.material_requests (
  request_id bigint generated by default as identity primary key,
  request_number text unique not null,
  project_id bigint references public.projects(project_id) on delete cascade not null,
  material_id bigint references public.materials_master(material_id) on delete restrict not null,
  
  -- Request details
  requested_quantity numeric(12,3) not null,
  request_source text not null check (request_source in ('Store', 'Local Procurement')),
  priority text default 'Normal' check (priority in ('Urgent', 'High', 'Normal', 'Low')),
  required_by date,
  purpose text,
  
  -- Approval workflow
  status text default 'Pending' not null check (status in ('Pending', 'Approved', 'Rejected', 'Fulfilled', 'Cancelled')),
  approved_by uuid references auth.users(id),
  approved_at timestamp with time zone,
  approval_notes text,
  
  -- Fulfillment tracking
  fulfilled_quantity numeric(12,3),
  fulfilled_at timestamp with time zone,
  fulfillment_notes text,
  
  created_at timestamp with time zone default now() not null,
  created_by uuid references auth.users(id)
);

-- Material Request Fulfillment Items (which units were used)
create table if not exists public.request_fulfillment_items (
  fulfillment_item_id bigint generated by default as identity primary key,
  request_id bigint references public.material_requests(request_id) on delete cascade not null,
  variant_id bigint references public.material_variants(variant_id) on delete restrict not null,
  units_issued int not null, -- How many units of this variant
  quantity_issued numeric(12,3) not null, -- Total quantity from these units
  issued_at timestamp with time zone default now() not null,
  issued_by uuid references auth.users(id)
);

-- Material Returns (from projects back to store)
create table if not exists public.material_returns (
  return_id bigint generated by default as identity primary key,
  return_number text unique not null,
  project_id bigint references public.projects(project_id) on delete cascade not null,
  material_id bigint references public.materials_master(material_id) on delete restrict not null,
  variant_id bigint references public.material_variants(variant_id),
  
  -- Return details
  returned_quantity numeric(12,3) not null,
  number_of_units int,
  condition text check (condition in ('Excellent', 'Good', 'Fair', 'Damaged', 'Unusable')),
  reason text,
  
  -- Store approval workflow
  status text default 'Pending' not null check (status in ('Pending', 'Accepted', 'Rejected')),
  reviewed_by uuid references auth.users(id),
  reviewed_at timestamp with time zone,
  review_notes text,
  
  -- Original request reference
  source_request_id bigint references public.material_requests(request_id),
  
  created_at timestamp with time zone default now() not null,
  created_by uuid references auth.users(id)
);

-- Material Movement Logs (Complete audit trail)
create table if not exists public.material_movement_logs (
  log_id bigint generated by default as identity primary key,
  material_id bigint references public.materials_master(material_id) on delete restrict not null,
  variant_id bigint references public.material_variants(variant_id),
  
  -- Movement details
  movement_type text not null check (movement_type in ('Store In', 'Store Out', 'Project In', 'Project Out', 'Return to Store', 'Local Procurement')),
  project_id bigint references public.projects(project_id),
  quantity numeric(12,3) not null,
  number_of_units int,
  
  -- References
  reference_type text check (reference_type in ('Material Request', 'Material Return', 'Manual Adjustment', 'Initial Stock')),
  reference_id bigint,
  
  -- Additional info
  notes text,
  movement_date timestamp with time zone default now() not null,
  created_by uuid references auth.users(id)
);

-- Auto-generate request numbers
create or replace function public.generate_request_number()
returns trigger
language plpgsql
as $$
declare
  v_sequence int;
  v_request_number text;
begin
  select coalesce(max(
    case 
      when request_number ~ '^MR-[0-9]+$' 
      then substring(request_number from '[0-9]+$')::int
      else 0
    end
  ), 0) + 1
  into v_sequence
  from public.material_requests;
  
  v_request_number := 'MR-' || lpad(v_sequence::text, 4, '0');
  new.request_number := v_request_number;
  
  return new;
end;
$$;

drop trigger if exists trg_generate_request_number on public.material_requests;
create trigger trg_generate_request_number
before insert on public.material_requests
for each row 
when (new.request_number is null or new.request_number = '')
execute function public.generate_request_number();

