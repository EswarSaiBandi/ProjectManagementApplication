-- Function: Update store inventory when request fulfilled
create or replace function public.update_store_inventory_on_fulfillment()
returns trigger
language plpgsql
as $$
begin
  if new.status = 'Fulfilled' and old.status != 'Fulfilled' then
    -- Log the movement
    insert into public.material_movement_logs (
      material_id,
      movement_type,
      project_id,
      quantity,
      reference_type,
      reference_id,
      notes,
      created_by
    ) values (
      new.material_id,
      case when new.request_source = 'Store' then 'Store Out' else 'Local Procurement' end,
      new.project_id,
      new.fulfilled_quantity,
      'Material Request',
      new.request_id,
      'Fulfilled request: ' || new.request_number,
      new.approved_by
    );
  end if;
  
  return new;
end;
$$;

drop trigger if exists trg_update_inventory_on_fulfillment on public.material_requests;
create trigger trg_update_inventory_on_fulfillment
after update on public.material_requests
for each row execute function public.update_store_inventory_on_fulfillment();

-- Function: Handle material returns to store
create or replace function public.handle_material_return_to_store()
returns trigger
language plpgsql
as $$
begin
  if new.status = 'Accepted' and old.status = 'Pending' then
    -- Add back to store inventory if variant specified
    if new.variant_id is not null then
      insert into public.store_inventory (
        material_id,
        variant_id,
        number_of_units,
        total_quantity,
        notes
      ) values (
        new.material_id,
        new.variant_id,
        coalesce(new.number_of_units, 0),
        new.returned_quantity,
        'Returned from project via ' || new.return_number
      )
      on conflict (material_id, variant_id) 
      do update set
        number_of_units = store_inventory.number_of_units + excluded.number_of_units,
        total_quantity = store_inventory.total_quantity + excluded.total_quantity,
        last_updated = now();
    end if;
    
    -- Log the movement
    insert into public.material_movement_logs (
      material_id,
      variant_id,
      movement_type,
      project_id,
      quantity,
      number_of_units,
      reference_type,
      reference_id,
      notes,
      created_by
    ) values (
      new.material_id,
      new.variant_id,
      'Return to Store',
      new.project_id,
      new.returned_quantity,
      new.number_of_units,
      'Material Return',
      new.return_id,
      'Accepted return: ' || new.return_number,
      new.reviewed_by
    );
  end if;
  
  return new;
end;
$$;

drop trigger if exists trg_handle_return_to_store on public.material_returns;
create trigger trg_handle_return_to_store
after update on public.material_returns
for each row execute function public.handle_material_return_to_store();

